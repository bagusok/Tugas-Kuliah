package prak1;

public class Nasabah {
    String norek, nama, alamat;
    int umur;
    double saldo;   

    Nasabah(String norek, String nama, String alamat, int umur, double saldo) {
        this.norek = norek;
        this.nama = nama;
        this.alamat = alamat;
        this.umur = umur;
        this.saldo = saldo;
    }
     
    Nasabah() {}



}
